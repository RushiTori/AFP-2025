import Foundation
/*:
 ## While Loops

 Vous venez de naître ! Toutes nos félicitations et bienvenue sur terre, humain.

 Pour afficher votre âge chaque année, utilisez une boucle **while** jusqu'à vos 18 ans.

 Imprimez "Bon anniversaire, vous avez ** ans" pour chaque année jusqu'à avoir vos 18 ans. À vos 18 ans, imprimez "Bon anniversaire, vous avez 18 ans, vous êtes donc majeur".
 */
var born = 0

while born < 18 {
    print("Bon anniversaire, vous avez \(born) ans")
    born += 1
}

print("Bon anniversaire, vous avez 18 ans, vous êtes donc majeur")




